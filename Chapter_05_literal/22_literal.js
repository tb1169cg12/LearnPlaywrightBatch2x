let age ="pramod"; // age is a string literal
console.log(age);  // Output: pramod
console.log(typeof age); // Output: string

let isStudent = true; // isStudent is a boolean literal
console.log(isStudent);  // Output: true
console.log(typeof isStudent); // Output: boolean

let pi = 3.14; // pi is a number literal
console.log(pi);  // Output: 3.14
console.log(typeof pi); // Output: number

let nullValue = null; // nullValue is a null literal
console.log(nullValue);  // Output: null
console.log(typeof nullValue); // Output: object (this is a known quirk in JavaScript)

let undefinedValue; // undefinedValue is an undefined literal (not assigned)
console.log(undefinedValue);  // Output: undefined
console.log(typeof undefinedValue); // Output: undefined